public class Location {
    private int row;
    private int col;
    
    public Location(int row, int col) {
        this.row = row;
        this.col = col;
    }
    
    //Retrieves Row.
    public int getRow() {
        return row;
    }
    //Retrieves Column.
    public int getCol() { 
        return col;
    }

    
}
